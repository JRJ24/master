Create table Contactos(
    ID_Contact NUMBER(10) 
    CONSTRAINT ID_CONTACTO_PK PRIMARY KEY,
    NAME_CONTACT VARCHAR2(30)
    CONSTRAINT NAME_CONTACT_NN NOT NULL,
    PHONE_NUMBER VARCHAR2(10)
    CONSTRAINT PHONE_NUMBER_NN NOT NULL,
    EMAIL VARCHAR2(25)
    CONSTRAINT EMAIL_NN NOT NULL,
    ADDRESS VARCHAR2(25)
    CONSTRAINT ADDRESS_NN NOT NULL,
    BIRTHDATE DATE
    CONSTRAINT BIRTHDATE_NN NOT NULL,
    NOTES VARCHAR2(255),
    GROUP_Contact VARCHAR2(40)
    CONSTRAINT GROUP_NN NOT NULL,
    DATE_LAST_CONCTACT DATE 
    CONSTRAINT DATE_LAST_CONCTACT_NN NOT NULL
);

